CREATE OR REPLACE PACKAGE BODY pkg_order_management IS

-- ============================================================
-- Name: calculate_order_total
-- Purpose: Computes the complete total price of items in an order [cite: 159]
-- Author: Group Member [cite: 159]
-- Date: 05-JUN-2026 [cite: 159]
-- Params: p_order_id IN NUMBER -- Target Order Identifier [cite: 159]
-- Notes: Contains explicit cursor parsing loops [cite: 159]
-- ============================================================
FUNCTION calculate_order_total(p_order_id IN NUMBER) RETURN NUMBER IS
    v_total NUMBER(10,2) := 0;
    
    -- Explicit Cursor Requirement [cite: 96, 159]
    CURSOR cur_order_items IS 
        SELECT oi.quantity, p.price 
        FROM order_items oi
        JOIN products p ON oi.product_id = p.product_id
        WHERE oi.order_id = p_order_id;
        
    -- User-defined Record Type Requirement [cite: 97, 159]
    TYPE item_summary_rec IS RECORD (
        qty   NUMBER,
        price NUMBER(10,2)
    );
    v_item item_summary_rec;
BEGIN
    OPEN cur_order_items;
    LOOP
        FETCH cur_order_items INTO v_item;
        EXIT WHEN cur_order_items%NOTFOUND;
        v_total := v_total + (v_item.qty * v_item.price);
    END LOOP;
    CLOSE cur_order_items;
    
    RETURN v_total;
EXCEPTION
    WHEN NO_DATA_FOUND THEN -- Predefined Exception [cite: 102]
        RETURN 0;
    WHEN OTHERS THEN
        IF cur_order_items%ISOPEN THEN
            CLOSE cur_order_items;
        END IF;
        RAISE_APPLICATION_ERROR(-20002, 'Error calculating total.');
END calculate_order_total;


-- ============================================================
-- Name: add_item_to_cart
-- Purpose: Inserts or alters quantities inside a client's cart [cite: 159]
-- Author: Group Member [cite: 159]
-- Date: 05-JUN-2026 [cite: 159]
-- Params: p_cart_id IN NUMBER -- Client Cart reference ID [cite: 159]
--         p_product_id IN NUMBER -- Selected product reference ID [cite: 159]
--         p_quantity IN NUMBER -- Amount to pass over [cite: 159]
-- Notes: Throws user-defined rule validation exceptions [cite: 159]
-- ============================================================
PROCEDURE add_item_to_cart(
    p_cart_id    IN NUMBER,
    p_product_id IN NUMBER,
    p_quantity   IN NUMBER
) IS
    v_exists NUMBER;
BEGIN
    -- Enforce rule with our user-defined exception [cite: 94, 103]
    IF p_quantity <= 0 THEN
        RAISE e_invalid_quantity;
    END IF;

    SELECT COUNT(*) INTO v_exists FROM cart_items 
    WHERE cart_id = p_cart_id AND product_id = p_product_id;

    IF v_exists > 0 THEN
        UPDATE cart_items 
        SET quantity = quantity + p_quantity
        WHERE cart_id = p_cart_id AND product_id = p_product_id;
    ELSE
        INSERT INTO cart_items (cart_id, product_id, quantity)
        VALUES (p_cart_id, p_product_id, p_quantity);
    END IF;
EXCEPTION
    WHEN e_invalid_quantity THEN
        RAISE_APPLICATION_ERROR(-20001, 'Quantity must be greater than zero.');
    WHEN OTHERS THEN
        RAISE_APPLICATION_ERROR(-20003, 'Database operation failed during cart insert.');
END add_item_to_cart;


-- ============================================================
-- Name: process_bulk_checkout
-- Purpose: Moves active cart items to permanent order history records [cite: 159]
-- Author: Group Member [cite: 159]
-- Date: 05-JUN-2026 [cite: 159]
-- Params: p_cart_id IN NUMBER -- Active cart target [cite: 159]
--         p_user_id IN NUMBER -- User context referencing mapping [cite: 159]
--         p_new_order_id OUT NUMBER -- Newly generated order container ID [cite: 159]
-- Notes: Uses Cursor FOR loops, nested tables, and subprogram calling [cite: 159]
-- ============================================================
PROCEDURE process_bulk_checkout(
    p_cart_id     IN NUMBER,
    p_user_id     IN NUMBER,
    p_new_order_id OUT NUMBER
) IS
    -- Collections Definitions [cite: 98]
    v_prod_list  product_arr;
    v_qty_list   quantity_tab := quantity_tab();
    v_idx        NUMBER := 1;
    v_final_cost NUMBER(10,2);
    
    -- %ROWTYPE Record Requirement [cite: 97]
    v_cart_rec   cart_items%ROWTYPE;
BEGIN
    -- Initializing base empty order tracker wrapper
    INSERT INTO orders (user_id, total_amount, status)
    VALUES (p_user_id, 0.00, 'Pending')
    RETURNING order_id INTO p_new_order_id;

    -- Cursor FOR loop processing [cite: 96]
    FOR v_item IN (SELECT * FROM cart_items WHERE cart_id = p_cart_id) LOOP
        -- Map values into memory structures
        v_prod_list(v_idx) := v_item.product_id;
        v_qty_list.EXTEND; -- Collection method usage [cite: 99]
        v_qty_list(v_idx)  := v_item.quantity;
        
        -- Batch push line item array sets
        INSERT INTO order_items(order_id, product_id, quantity)
        VALUES (p_new_order_id, v_prod_list(v_idx), v_qty_list(v_idx));
        
        v_idx := v_idx + 1;
    END LOOP;

    -- Call to internal function (Requirement: Subprogram calling another subprogram) [cite: 80]
    v_final_cost := calculate_order_total(p_new_order_id);
    
    UPDATE orders SET total_amount = v_final_cost WHERE order_id = p_new_order_id;
    
    -- Clean up cart items using collection method check [cite: 99]
    IF v_prod_list.EXISTS(1) THEN
        DELETE FROM cart_items WHERE cart_id = p_cart_id;
    END IF;

EXCEPTION
    WHEN TOO_MANY_ROWS THEN -- Predefined Exception [cite: 102]
        RAISE_APPLICATION_ERROR(-20004, 'Data integrity error encountered during process execution.');
    WHEN OTHERS THEN
        RAISE_APPLICATION_ERROR(-20005, 'Checkout processing system exception raised.');
END process_bulk_checkout;

END pkg_order_management;
/


-- ============================================================
-- Name: trg_products_before_insert
-- Purpose: Ensures product names are properly uppercase for formatting consistency [cite: 159]
-- Table: products [cite: 159]
-- Event: BEFORE INSERT [cite: 92, 159]
